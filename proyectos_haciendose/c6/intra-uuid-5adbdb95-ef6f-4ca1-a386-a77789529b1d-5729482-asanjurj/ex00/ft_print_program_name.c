#include <unistd.h>

void	ft_putchar(char *c)
{
	int	i;

	i = 0;
	while (c[i] != '\0')
	{
		write(1, &c[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	if (argc)
	{
		ft_putchar(argv[0]);
		write(1, "\n", 1);
		return (0);
	}
}
